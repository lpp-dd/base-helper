    public static void main(String[] args) {
        //使用hash表来实现  判断差值是否存在
        int[] arr = new int[]{};
        Map<Integer, Integer> map = new HashMap<>();
        int target = 10;
        for (int i = 0; i < arr.length; i++) {
            int value = arr[i];
            int chazhi = target - value;
            if (map.containsKey(chazhi)) {
                System.out.println(map.get(chazhi));
                System.out.println(i);
                return;
            }
            map.put(value, i);
        }
    }

